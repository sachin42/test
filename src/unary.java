//Unary operators
public class unary {
    public static void main(String[] args) {
        int i = 10;
        System.out.println(++i);        // inc 1 and print 11,      i = 11
        System.out.println(i++);        // print 11 and inc 1,      i = 12
        System.out.println(i--);        // print 12 and dec 1,      i = 11
        System.out.println(--i);        // dec 1 and print 10,      i = 10
    }
}

public class unary {
    public static void main(String[] args) {
        int i = 10;
        System.out.println(++i); // inc and print 11, i = 11
        System.out.println(i++); // print and inc 11, i = 12
        System.out.println(i--); // print and dec 12, i = 11
        System.out.println(--i); // dec and print 10, i = 10
    }
}
